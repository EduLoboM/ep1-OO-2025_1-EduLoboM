public class Pessoa {
    String nomeP;
    int id;

    public void setNomeP (String nomeP){
        this.nomeP = nomeP;
    }

    public void setId (int id){
        this.id = id;
    }

    public String getNomeP (){
        return this.nomeP;
    }

    public int getId (){
        return this.id;
    }
}
